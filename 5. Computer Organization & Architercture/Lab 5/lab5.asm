TITLE ASK5
ASSUME CS:KODIKAS, DS:DEDOMENA, SS:SOROS
KODIKAS SEGMENT PUBLIC

MAIN PROC NEAR
    MOV AX,DEDOMENA
    MOV DS,AX

    MOV AL,NUM1
    ADD AL,NUM2

    MOV DL,AL
    CALL DISPLAY_HEX;

    MOV AH,4CH
    INT 21H
MAIN ENDP

DISPLAY_HEX PROC NEAR
	MOV BL,DL

	MOV BH,0
	MOV CL,4

	SHL BX,CL
	MOV DL,BH

	CALL ONE_DIGIT

	MOV CL,4

	SHR BL,CL
	MOV DL,BL

	CALL ONE_DIGIT

	RET	
DISPLAY_HEX ENDP

ONE_DIGIT PROC NEAR
	CMP DL,9
	JA GRAMMA

	ADD DL,48
	JMP NEXT

    GRAMMA:
        ADD DL,'A'-10

    NEXT:
        MOV AH,02H
        INT 21H

    TELOS:
        RET
ONE_DIGIT ENDP

KODIKAS ENDS

DEDOMENA SEGMENT
	NUM1 DB 1Fh
	NUM2 DB 0B3h
DEDOMENA ENDS

SOROS SEGMENT STACK
	DB 256 DUP(0)
SOROS ENDS

END MAIN